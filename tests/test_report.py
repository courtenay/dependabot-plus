class TestPostReport:
    def test_calls_gh_with_correct_arguments(self):
        item = _make_item()
        verdict = Verdict(
            risk_level=RiskLevel.LOW,
            static_findings=StaticFindings(),
            dynamic_findings=SandboxResult(
                install_exit_code=0, install_logs=""
            ),
            summary="All clear.",
        )

        with patch("dependabot_plus.report.github.subprocess.run") as mock_run:
            mock_run.return_value = MagicMock(returncode=0)
            post_report(item, verdict)

        mock_run.assert_called_once()
        call_args = mock_run.call_args
        cmd = call_args[0][0]

        assert cmd[0] == "gh"
        assert cmd[1] == "pr"
        assert cmd[2] == "comment"
        assert cmd[3] == "99"
        assert "--repo" in cmd
        assert cmd[cmd.index("--repo") + 1] == "owner/repo"
        assert "--body" in cmd
        # The body should be the formatted report
        body = cmd[cmd.index("--body") + 1]
        assert "lodash" in body
        assert "All clear." in body

        # Verify subprocess options
        assert call_args[1]["capture_output"] is True
        assert call_args[1]["text"] is True
        assert call_args[1]["check"] is True

    def test_raises_on_gh_failure(self):
        item = _make_item()
        verdict = Verdict(
            risk_level=RiskLevel.LOW,
            static_findings=StaticFindings(),
            dynamic_findings=SandboxResult(
                install_exit_code=0, install_logs=""
            ),
            summary="Ok.",
        )

        with patch("dependabot_plus.report.github.subprocess.run") as mock_run:
            mock_run.side_effect = subprocess.CalledProcessError(1, "gh")
            with pytest.raises(subprocess.CalledProcessError):
                post_report(item, verdict)
